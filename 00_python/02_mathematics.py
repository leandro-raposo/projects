
# Operações matemáticas em Python
calculo = 2 + 2
print(calculo)

calculo = 2 + 2 * 3
print(calculo)

calculo = 2 + 2 * 3 / 2
print(calculo)

calculo = (2 + 2) * 3 / 2
print(calculo)

calculo = 2 ** 3 + 2 * 3 / 2
print(calculo)

